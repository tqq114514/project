package day04;

public class MyPracticeForCircle {
    public static void main(String[] args) {
        for(int i=0;i<=4;i++){
            System.out.println("行动是成功的阶梯");
        }
        System.out.println("\n");
        for(int i=1;i<=9;i++){
            System.out.println(i+"*9="+9*i);
        }
        System.out.println("\n");
        for (int i=1;i<=9;i+=2){
            System.out.println(i+"*9="+9*i);
        }
        System.out.println("\n");
        for (int i=9;i>=1;i--){
            System.out.println(i+"*9="+9*i);
        }
    }
}
